            <p>WUG - Stands for Wireless User Group. There are many wifi groups around South Africa and here you will be able to find a way to connect to one nearest to you or even start your own.</p>
            <p>All WUGs listed here are non-profit communities, that do not offer any commercial services such as Internet, they merely offer an infrastructure for people to exchange information and data and play games.</p>
            <p>All equipment is standard wifi equipment, operating under 802.11 standards which makes it affordable to anyone. Joining and setting up your node is more a learning experience than a chance to leech files, you will learn a lot...</p>
            <p>The goal of wug.za.net is to integrate all the WUGs around the country and potentially link them together in the future.</p> 
